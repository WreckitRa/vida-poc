/**
 * Check if a location (city or area) exists in the database
 * Returns true if the location matches any city or area in the database
 */
export function isLocationValid(location: string): boolean {
  if (!location || location.trim() === "") return false;
  const locationLower = location.toLowerCase().trim();
  
  return RESTAURANTS.some((r) => 
    r.city.toLowerCase().includes(locationLower) ||
    locationLower.includes(r.city.toLowerCase()) ||
    r.area.toLowerCase().includes(locationLower) ||
    locationLower.includes(r.area.toLowerCase())
  );
}

/**
 * Check if a cuisine exists in the database (fuzzy matching)
 * Returns true if the cuisine matches any cuisine in the database
 */
export function isCuisineValid(cuisine: string): boolean {
  if (!cuisine || cuisine.trim() === "") return false;
  const cuisineLower = cuisine.toLowerCase().trim();
  const availableCuisines = getAvailableCuisines();
  
  return availableCuisines.some((c) => 
    c.toLowerCase().includes(cuisineLower) ||
    cuisineLower.includes(c.toLowerCase())
  );
}

