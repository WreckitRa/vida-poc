/**
 * Find candidate restaurants based on user preferences
 * Returns top 5 ranked candidates with scores
 */
export function findCandidates(preferences: UserPreferences): Candidate[] {
  const candidates: Candidate[] = RESTAURANTS.map((restaurant) => {
    let score = 0;

    // Cuisine matching (+3 per match, but only if there's an actual match)
    if (preferences.cuisines && preferences.cuisines.length > 0) {
      const cuisineMatches = restaurant.cuisines.filter((c) =>
        preferences.cuisines!.some((pref) => {
          const cLower = c.toLowerCase();
          const prefLower = pref.toLowerCase();
          // Exact match or substring match (but be more strict)
          return cLower === prefLower || 
                 cLower.includes(prefLower) || 
                 prefLower.includes(cLower);
        })
      );
      // Only add score if there's a match - don't give points for no matches
      if (cuisineMatches.length > 0) {
        score += cuisineMatches.length * 3;
      } else {
        // Penalize restaurants that don't match the requested cuisine
        score -= 5;
      }
    }

    // Vibe matching (+2 per match)
    if (preferences.vibe && preferences.vibe.length > 0) {
      const vibeMatches = restaurant.vibe.filter((v) =>
        preferences.vibe!.some((pref) =>
          v.toLowerCase().includes(pref.toLowerCase()) ||
          pref.toLowerCase().includes(v.toLowerCase())
        )
      );
      score += vibeMatches.length * 2;
    }

    // Dietary matching (+2 per match)
    if (preferences.dietary && preferences.dietary.length > 0) {
      const dietaryMatches = restaurant.dietary.filter((d) =>
        preferences.dietary!.some((pref) =>
          d.toLowerCase().includes(pref.toLowerCase()) ||
          pref.toLowerCase().includes(d.toLowerCase())
        )
      );
      score += dietaryMatches.length * 2;
    }

    // Budget matching (+3 for exact match)
    if (preferences.budget) {
      if (restaurant.price === preferences.budget) {
        score += 3;
      }
    }

    // Location matching (+2 for city, +3 for area)
    if (preferences.location) {
      const locationLower = preferences.location.toLowerCase();
      if (restaurant.city.toLowerCase().includes(locationLower) ||
          locationLower.includes(restaurant.city.toLowerCase())) {
        score += 2;
      }
      if (restaurant.area.toLowerCase().includes(locationLower) ||
          locationLower.includes(restaurant.area.toLowerCase())) {
        score += 3;
      }
    }

    // Rating weight (multiply by rating, then add)
    score += restaurant.rating * 0.5;

    // Bonus for booking availability (+1)
    if (restaurant.bookingAvailable) {
      score += 1;
    }

    return {
      restaurant,
      score: Math.round(score * 100) / 100, // Round to 2 decimal places
    };
  });

  // Sort by score descending and return top 5
  return candidates
    .sort((a, b) => b.score - a.score)
    .slice(0, 5);
}
