/**
 * Get all unique cities from the restaurant database
 */
export function getAvailableCities(): string[] {
  const cities = new Set<string>();
  RESTAURANTS.forEach((r) => cities.add(r.city));
  return Array.from(cities).sort();
}

/**
 * Get all unique cuisines from the restaurant database
 */
export function getAvailableCuisines(): string[] {
  const cuisines = new Set<string>();
  RESTAURANTS.forEach((r) => r.cuisines.forEach((c) => cuisines.add(c)));
  return Array.from(cuisines).sort();
}

